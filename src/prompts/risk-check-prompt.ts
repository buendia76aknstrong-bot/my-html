export function buildRiskCheckPrompt(manuscriptText: string): string {
  return `あなたは自分史のリスク審査AIです。
以下の原稿をスキャンし、3つのリスクカテゴリに該当する表現を検出・修正してください。

【リスクカテゴリ】
1. 第三者リスク: 実名・特定可能な人物（会社名+役職、近隣住民の特定等）
2. 差別リスク: 国籍・民族・性別・障害・宗教への否定的表現
3. 名誉リスク: 他者の不正・犯罪・不倫・借金等の断定的記述

【修正ルール】
- 実名 → 「友人」「上司」「恩師」等の関係性表現に置換
- 会社名+役職 → 「当時の職場で」等に置換
- 差別表現 → 主観・学び表現に変換（例:「あの国の人は信用できない」→「価値観の違いに戸惑った」）
- 犯罪断定 → 「疑問に感じる出来事があった」等に軟化
- 修正箇所は【修正ログ】として一覧出力

【入力原稿】
${manuscriptText}

【出力形式】
以下のJSON形式で出力してください（JSONのみ、説明文不要）:

{
  "correctedContent": "修正後の原稿全文をここに記載",
  "log": [
    {
      "category": "third_party または discrimination または defamation",
      "original": "修正前の表現",
      "modified": "修正後の表現",
      "reason": "修正理由"
    }
  ]
}

修正箇所がない場合は log を空配列にし、correctedContent に原文をそのまま記載してください。`;
}
